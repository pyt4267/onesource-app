"use client";

import React from 'react';

interface GeneratedContent {
    summary: string;
    tiktok: { hook: string; body: string; cta: string };
    xThread: string[];
    linkedin: string;
    note: string;
    watermark: boolean;
}

const Card = ({ children, style, copyText }: { children: React.ReactNode, style?: React.CSSProperties, copyText?: string }) => {
    const [copied, setCopied] = React.useState(false);

    const handleCopy = async () => {
        if (copyText) {
            await navigator.clipboard.writeText(copyText);
            setCopied(true);
            setTimeout(() => setCopied(false), 2000);
        }
    };

    return (
        <div
            style={{
                background: "var(--glass-surface)",
                border: "1px solid var(--glass-border)",
                borderRadius: "24px",
                padding: "1.5rem",
                backdropFilter: "blur(12px)",
                color: "#fff",
                overflow: "hidden",
                position: "relative",
                boxShadow: "0 4px 6px rgba(0,0,0,0.1)",
                transition: "transform 0.2s ease, border-color 0.2s ease",
                cursor: copyText ? "pointer" : "default",
                ...style
            }}
            onClick={handleCopy}
            onMouseEnter={(e) => {
                e.currentTarget.style.transform = "translateY(-5px)";
                e.currentTarget.style.borderColor = "rgba(168, 85, 247, 0.4)";
            }}
            onMouseLeave={(e) => {
                e.currentTarget.style.transform = "translateY(0)";
                e.currentTarget.style.borderColor = "var(--glass-border)";
            }}
        >
            {copyText && (
                <div style={{
                    position: "absolute",
                    top: "0.75rem",
                    right: "0.75rem",
                    fontSize: "0.7rem",
                    background: copied ? "#22c55e" : "rgba(255,255,255,0.1)",
                    padding: "4px 8px",
                    borderRadius: "6px",
                    color: copied ? "white" : "#a1a1aa"
                }}>
                    {copied ? "Copied!" : "Click to copy"}
                </div>
            )}
            {children}
        </div>
    );
};

